def tambah(bil1, bil2):
    return bil1 + bil2

def kurang(bil1, bil2):
    return bil1 - bil2

def kali(bil1, bil2):
    return bil1 * bil2

def bagi(bil1, bil2):
    return bil1 / bil2

x = 15
y = 10

print("Nilai x : ", x)
print("Nilai y : ", y)

print("Hasil tambah : ", tambah(x, y))
print("Hasil kurang : ",kurang(x, y))
print("Hasil kali : ",kali(x, y))
print("Hasil bagi : ",bagi(x, y))
